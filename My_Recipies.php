        <h3>Aloo Paratha</h3>
        <p>First prepare dough by taking 1 cup wheat flour, 1 teaspoon oil and salt in a small bowl. Incrementally add water in small quantities and knead smooth and soft dough (like chapati dough). Grease its surface with 1/2 teaspoon oil, cover it with clean muslin cloth and set aside for 15-20 minutes to settle at room temperature.<br>
            step-1 <br>
            Now, prepare alu paratha stuffing. Take the boiled potatoes in a medium bowl and mash it until no chunks of potato. Add chopped green chillies, red chilli powder, grated ginger, garam masala powder, sugar, lemon juice and salt. Mix all ingredients properly. <br>
            step-2 <br>
            Divide stuffing into 6 equal parts and give them a shape of ball. Divide prepared dough into 6 equal parts, give them a round shape and press a little to flatten like pattie. <br>
            step-3 <br>
            Take 1/2 cup dry wheat flour in a small plate for dusting. Take one dough pattie and coat it with dry wheat flour on both sides. (It will prevent dough from sticking to rolling board). Put it on rolling board/roti making board (chakla) and roll it out into a circle of approx. 4-5 inch diameter using rolling pin. Put one portion of stuffing in the center of it. <br>
            step-4 <br>
            Wrap stuffing ball by lifting all sides of rolled circle towards top, seal the edges and again give it a round ball shape. <br>
        </p>
